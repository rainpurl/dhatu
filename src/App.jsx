/* ---------- speech (browser TTS, degrades gracefully) ---------- */
let _voiceCache = [];
function _loadVoices() {
  try {
    if (typeof window === "undefined" || !window.speechSynthesis) return [];
    const vs = window.speechSynthesis.getVoices() || [];
    if (vs.length) _voiceCache = vs;
    return vs;
  } catch (e) {
    return [];
  }
}
if (typeof window !== "undefined" && window.speechSynthesis) {
  _loadVoices();
  try {
    window.speechSynthesis.onvoiceschanged = _loadVoices;
  } catch (e) {}
}
const _GOOD_VOICE_HINTS = ["google", "natural", "neural", "aria", "jenny", "samantha", "wavenet", "premium"];
function _pickVoice(lang) {
  const vs = _voiceCache.length ? _voiceCache : _loadVoices();
  if (!vs.length) return null;
  const pre = lang.slice(0, 2).toLowerCase();
  const exact = vs.filter((v) => v.lang && v.lang.toLowerCase() === lang.toLowerCase());
  const partial = vs.filter((v) => v.lang && v.lang.toLowerCase().startsWith(pre));
  const pool = exact.length ? exact : partial;
  if (!pool.length) return null;
  for (const hint of _GOOD_VOICE_HINTS) {
    const hit = pool.find((v) => v.name && v.name.toLowerCase().includes(hint));
    if (hit) return hit;
  }
  const network = pool.find((v) => v.localService === false);
  return network || pool[0];
}
/* Pre-recorded audio, generated by scripts/generate-audio.mjs into
   public/audio/. If a phrase has a recorded file we play that (crisp, works
   offline, and gives Gujarati a real voice on devices that have no gu-IN TTS).
   If there is no manifest or no matching file, we fall back to browser TTS,
   so the app behaves exactly as before until audio has been generated. */
const AUDIO_BASE = "/audio/";
let _audioManifest = null;
let _audioTried = false;
let _curAudio = null;
function _loadAudioManifest() {
  if (_audioTried || typeof window === "undefined" || !window.fetch) return;
  _audioTried = true;
  try {
    window
      .fetch(AUDIO_BASE + "manifest.json")
      .then((r) => (r.ok ? r.json() : null))
      .then((j) => { if (j && j.items) _audioManifest = j.items; })
      .catch(() => {});
  } catch (e) {}
}
if (typeof window !== "undefined") _loadAudioManifest();
function _audioFileFor(text, lang) {
  if (!_audioManifest) return null;
  const key = lang.slice(0, 2).toLowerCase() + "|" + text;
  return _audioManifest[key] || null;
}
function _speakTTS(text, lang) {
  try {
    if (typeof window === "undefined" || !window.speechSynthesis) return;
    window.speechSynthesis.cancel();
    const u = new SpeechSynthesisUtterance(text);
    u.lang = lang;
    const v = _pickVoice(lang);
    if (v) u.voice = v;
    const pre = lang.slice(0, 2).toLowerCase();
    u.rate = pre === "gu" ? 0.86 : 0.97;
    u.pitch = 1;
    window.speechSynthesis.speak(u);
  } catch (e) {}
}
function speak(text, lang = "gu-IN") {
  try {
    stopSpeak();
    const file = _audioFileFor(text, lang);
    if (file) {
      const a = new Audio(AUDIO_BASE + file);
      _curAudio = a;
      a.onended = () => { if (_curAudio === a) _curAudio = null; };
      a.onerror = () => { if (_curAudio === a) _curAudio = null; _speakTTS(text, lang); };
      const p = a.play();
      if (p && p.catch) p.catch(() => { if (_curAudio === a) { _curAudio = null; _speakTTS(text, lang); } });
      return;
    }
    _speakTTS(text, lang);
  } catch (e) {
    _speakTTS(text, lang);
  }
}
function speakEn(text) {
  speak(text, "en-US");
}
function speakGu(text) {
  speak(text, "gu-IN");
}
function stopSpeak() {
  try {
    if (typeof window !== "undefined" && window.speechSynthesis) window.speechSynthesis.cancel();
  } catch (e) {}
  try {
    if (_curAudio) { _curAudio.pause(); _curAudio = null; }
  } catch (e) {}
}
const shuffle = (a) => {
  const b = a.slice();
  for (let i = b.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [b[i], b[j]] = [b[j], b[i]];
  }
  return b;
};
